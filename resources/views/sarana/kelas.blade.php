        <h1 align=center>Kelas</h1>
        <p>
            Jurusan Teknologi Informasi memiliki 2 kriteria ruang kelas yang digunakan untuk melakukan sesi perkuliahan teori. 
            <br>Dua kriteria tersebut antara lain Kelas Reguler (KR) dan Kelas Bersama (KB). KR digunakan untuk perkuliahan dengan 
            <br>peserta kuliah sebanyak 1 kelas (kapasitas ruangan hanya kurang lebih untuk 30 mahasiswa), sedangkan KB digunakan untuk 
            <br>kuliah bersama dari 2 kelas (kapasitas ruangan sebanyak 60 mahasiswa). Lokasi KR dan KB tersebar di 2 gedung, baik 
            <br>gedung AF maupun gedung T.Sipil lt. 7 dan 8.
        </p>
